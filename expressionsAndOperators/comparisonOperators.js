//Equal To
//non-strict check for the value alone to match
let equalTo = "3" == 3;
console.log(equalTo);

//Strict Equals To
//Strict checks for data type as well as the value to match
let strictEqualTo = "3" === 3;
console.log(strictEqualTo);

//Not Equal To
let notEqualTo = 3 != "3"
console.log(notEqualTo);

//Strict Not Equal To
let strictNotEqualTo = 3 !== "3";
console.log(strictNotEqualTo);

//Greater Than
let greaterThan = 4 > 3;
console.log(greaterThan);

//Less Than
let lessThan = 33 < 100;
console.log(lessThan);

//Greater Than or Equal To
let greaterThanOrEqualTo = 3 >= 3;
console.log(greaterThanOrEqualTo);

//Less Than or Equal To
let lessThanOrEqualTo = 2 <= 2;
console.log(lessThanOrEqualTo);

//And
2 + 2 && 3 + 3 

//Or
2 || 3

